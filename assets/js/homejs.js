    $('.line1').removeClass('initlinerv').addClass('initline')
    $('.line2').removeClass('initline2rv').addClass('initline2')
    $('.line3').removeClass('initline3rv').addClass('initline3')
    $('.line4').removeClass('initline4rv').addClass('initline4')
